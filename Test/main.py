
# ====================================================
# Import
# ====================================================

#======================
# Vendors
import sys

#======================
# Own
import window

# ====================================================
# Functions
# ====================================================


def start(x, y):
    exe = window.window(x, y)
    exe.run()

# ====================================================
# Entry Point
# ====================================================

if __name__ == "__main__":
    start(int(sys.argv[1]), int(sys.argv[2]))

# ====================================================
# End
# ====================================================
